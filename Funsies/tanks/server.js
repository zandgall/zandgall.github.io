// Add the WebSocket handlers

var players = {};

var fullbullets = {};

var kill = {};

io.on('connection', function(socket) {
    socket.on('bullet', function(data) {    
       fullbullets[socket.id] = data;
        if(kill[socket.id]!=-1){
            socket.emit('killthybullet', kill[socket.id]);
            kill[socket.id]=-1;
        }
    });
    socket.on('new player', function() {
        players[socket.id] = {};
    });
    socket.on('player', function(data) {
        players[socket.id] = data;
    });
    
    socket.on('killbullet', function(id, j) {
        kill[id]=j;
    });
    
});



setInterval(function() {
    io.sockets.emit('state', players);
    io.sockets.emit('stateb', fullbullets);
    io.sockets.emit('world', WORLDS[world]);
    for(var id in players){
        if(players[id]!=null&&players[id].ticked!=null) {
            players[id].ticked++;
            if(players[id].ticked>=90)
                console.log("Player", id, "disconnecting in", 100-players[id].ticked);
            if(players[id].ticked>=100) {
                
                for(var k in fullbullets){
                    for(var j = 0; j<fullbullets[k].length; j++){
                        if(fullbullets[k][j].id==players[id].id)
                            fullbullets[k][j].dead=true;
                    }
                }
                
                players[id]=null;
                console.log("Player disconnected", id);
            }
        }
    }
}, 1000 / 60);
